package buoi_1;

public class bai_tap_1 {
    public static void main(String[] args) {
        // Khai báo các biến
        String name = "Giang Que Man";   // thay bằng tên của bạn
        int age = 20;                   // thay bằng tuổi của bạn
        double height = 1.8;           // thay bằng chiều cao của bạn (mét)
        boolean isStudent = true;       // thay đổi theo đúng tình trạng của bạn

        // In ra màn hình
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + "m");
        System.out.println("Is Student: " + isStudent);
    }
}
